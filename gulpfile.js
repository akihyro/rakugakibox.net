/*====================================================================================================================
 * gulpfile
 *====================================================================================================================*/

var del = require("del");
var gulp = require("gulp");

/*--------------------------------------------------------------------------------------------------------------------
 * お掃除タスク
 *--------------------------------------------------------------------------------------------------------------------*/

gulp.task("clean", del.bind(null, "dist"));

/*--------------------------------------------------------------------------------------------------------------------
 * ビルドタスク
 *--------------------------------------------------------------------------------------------------------------------*/

gulp.task("build", [
    "build:images",
]);

/*--------------------------------------------------------------------------------------------------------------------
 * ビルドタスク: 画像
 *--------------------------------------------------------------------------------------------------------------------*/

gulp.task("build:images", function() {
    return gulp.src([
        "src/images/**/*",
        "!src/images/**/*.sai",
    ])
        .pipe(gulp.dest("dist/images"));
});
